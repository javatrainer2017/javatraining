public class OperatorCheckExcercise {

    public static void main(String[] args)
    {
        System.out.println("Hello World!!!");
        int i=10;
        int j=10;

        System.out.println(" + " + (i+j) );
        System.out.println(" - " + (i-j) );
        System.out.println(" * " + (i*j) );
        System.out.println(" / " + (i/j) );
        System.out.println(" % " + (i%j) );

        System.out.println(" ++ " + (i++) );
//        i = i + 1;
        System.out.println(" ++ " + (i) );
        System.out.println(" ++ " + (++i) );

        System.out.println(" ++ " + (i--) );
        System.out.println(" ++ " + (i) );
        System.out.println(" ++ " + (--i) );

        System.out.println(" += " + (i+=10));// i = i + 10

    }



}
